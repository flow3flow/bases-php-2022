    <h1>variables locales</h1>
    <p>En PHP, les variables sont représentées par un signe dollar "$" suivi du nom de la variable. Le nom est sensible à la casse.

Les noms de variables suivent les mêmes règles de nommage que les autres entités PHP. Un nom de variable valide doit commencer par une lettre ou un souligné (_),
 suivi de lettres, chiffres ou soulignés</p>
 <p>On va éviter les signes servant aux calculs (+-*/)</p>

    <h2>Utilité des variables</h2>
    <p>Elles servent à stocker des informations qui peuvent être manipulées, affichées , sauvegardées, détruites...</p>
